package Model;

public class wMarking {

    private int k;
    private int n;
    private int r;

    public int getK() {
        return k;
    }

    public void setK(int k) {
        this.k = k;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public String toString(){
        return k+"W"+n+" + "+r;
    }


}
